def load_heart_disease_data():
    """
    Loads the Heart Disease dataset.
    Tries the online URL first, then falls back to a local copy.
    Always returns a clean DataFrame.
    """
    # Reliable GitHub mirror of the Cleveland heart disease dataset
    url = ("https://raw.githubusercontent.com/"
           "sharmaroshan/Heart-UCI-Dataset/master/heart.csv")

    print("📥 Loading Heart Disease dataset...")

    try:
        df = pd.read_csv(url)
        print(f"   ✅ Loaded from online source")
    except Exception as e:
        print(f"   ⚠️  Online load failed ({e})")
        print("   📥 Trying secondary source...")
        try:
            url2 = ("https://raw.githubusercontent.com/"
                    "dsrscientist/dataset1/master/heart_disease.csv")
            df = pd.read_csv(url2)
            print("   ✅ Loaded from secondary source")
        except Exception:
            print("   ⚠️  Both sources failed. Creating sample dataset...")
            # Minimal reproducible dataset as a last fallback
            df = _create_sample_dataset()

    # ── Rename columns to clear names ─────────────────────────
    rename_map = {
        'age':'age', 'sex':'sex', 'cp':'chest_pain', 'trestbps':'blood_pressure',
        'chol':'cholesterol', 'fbs':'blood_sugar', 'restecg':'ecg',
        'thalach':'max_heart_rate', 'exang':'exercise_angina',
        'oldpeak':'st_depression', 'slope':'slope', 'ca':'vessels',
        'thal':'thal', 'target':'target'
    }
    # Only rename columns that exist in the DataFrame
    df.rename(columns={k: v for k, v in rename_map.items()
                       if k in df.columns}, inplace=True)

    # Make sure target is binary (0 or 1)
    if df['target'].max() > 1:
        df['target'] = (df['target'] > 0).astype(int)

    print(f"\n  ✅ Dataset ready!")
    print(f"     Rows     : {df.shape[0]} patients")
    print(f"     Columns  : {df.shape[1]} features")
    print(f"     Target 0 : {(df['target']==0).sum()} (No Disease)")
    print(f"     Target 1 : {(df['target']==1).sum()} (Heart Disease)")

    return df


