def predict_disease(*input_values):
    """
    Takes patient feature values from the Gradio sliders
    and returns a prediction from the best model.

    Returns:
        result_text (str)  — prediction + confidence
        probs_dict  (dict) — probability for bar chart
    """
    try:
        # Build feature array in the correct column order
        values = np.array(input_values, dtype=float).reshape(1, -1)

        # Scale if the best model needs it
        if BEST_MODEL_NAME in ['Logistic Regression', 'SVM']:
            values_input = scaler.transform(values)
        else:
            values_input = pd.DataFrame(values, columns=FEATURE_NAMES)

        # Get probability scores
        proba = best_model.predict_proba(values_input)[0]
        pred  = int(np.argmax(proba))

        disease_prob    = float(proba[1])
        no_disease_prob = float(proba[0])

        # Format result message
        if pred == 1:
            emoji  = "⚠️"
            verdict= "Heart Disease Detected"
            note   = "Please consult a cardiologist immediately."
        else:
            emoji  = "✅"
            verdict= "No Heart Disease Detected"
            note   = "Results look healthy. Maintain a healthy lifestyle."

        result_text = (
            f"{emoji} {verdict}\n\n"
            f"Disease Risk    : {disease_prob*100:.1f}%\n"
            f"Healthy Chance  : {no_disease_prob*100:.1f}%\n\n"
            f"Best model used : {BEST_MODEL_NAME}\n\n"
            f"⚕️  {note}\n\n"
            f"⚠️  This is an AI prediction only — "
            f"not a medical diagnosis."
        )

        probs_dict = {
            'No Disease'   : round(no_disease_prob, 4),
            'Heart Disease': round(disease_prob,    4),
        }

        return result_text, probs_dict

    except Exception as e:
        return f"❌ Prediction error: {str(e)}", {}


