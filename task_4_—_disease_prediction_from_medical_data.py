"""
# ────────────────────────────────────────────────────────────────
# CELL 6 — Preprocess the Data
#
# Steps:
#   1. Separate features (X) from target (y)
#   2. Handle any missing values (fill with median)
#   3. Scale numerical features using StandardScaler
#      (ML models work better when all features are on same scale)
# ─────────────────────────────────────────────────────"""

print("\n🔧 Preprocessing data...")

# Work on a clean copy
df = df_raw.copy()

# Step 1: Separate features and target
X = df.drop('target', axis=1)
y = df['target']

# Store feature names for later use
FEATURE_NAMES = list(X.columns)
print(f"\n   Features used ({len(FEATURE_NAMES)}):")
for i, name in enumerate(FEATURE_NAMES):
    print(f"     {i+1:2d}. {name}")

# Step 2: Handle missing values
missing_count = X.isnull().sum().sum()
if missing_count > 0:
    print(f"\n   ⚠️  Found {missing_count} missing values — filling with median")
    for col in X.columns:
        if X[col].isnull().any():
            X[col].fillna(X[col].median(), inplace=True)
else:
    print(f"\n   ✅ No missing values to handle")

# Step 3: Convert any non-numeric columns
for col in X.columns:
    if X[col].dtype == 'object':
        X[col] = LabelEncoder().fit_transform(X[col].astype(str))
        print(f"   Encoded column: {col}")

print(f"\n   X shape : {X.shape}")
print(f"   y shape : {y.shape}")
print(f"   X dtype : {X.dtypes.value_counts().to_dict()}")
print("\n  ✅ Preprocessing complete!")

"""
# ────────────────────────────────────────────────────────────────
# CELL 7 — Split into Train and Test Sets
#
# 80% training → model learns from this
# 20% testing  → model is evaluated on this (never seen before)
#
# stratify=y means both splits have same disease/no-disease ratio
# ───────────────────────────────────────────────────"""

X_train, X_test, y_train, y_test = train_test_split(
    X, y,
    test_size    = TEST_SIZE,
    random_state = RANDOM_STATE,
    stratify     = y           # keeps class balance in both splits
)

# Scale features — StandardScaler makes mean=0, std=1
# This is important for Logistic Regression and SVM
scaler  = StandardScaler()
X_train_scaled = scaler.fit_transform(X_train)   # fit on train only
X_test_scaled  = scaler.transform(X_test)         # apply same scale to test

print(f"\n📊 Data Split:")
print(f"   Training set  : {X_train.shape[0]} patients "
      f"({y_train.sum()} disease / "
      f"{(y_train==0).sum()} no disease)")
print(f"   Test set      : {X_test.shape[0]} patients "
      f"({y_test.sum()} disease / "
      f"{(y_test==0).sum()} no disease)")
print(f"\n  ✅ Train/Test split done!")

"""
# ────────────────────────────────────────────────────────────────
# CELL 8 — Train 4 Machine Learning Models
#
# Why 4 models?
#   We don't know in advance which algorithm will work best.
#   We train all of them and pick the winner based on metrics.
#
# Models:
#   1. Logistic Regression — fast, interpretable, good baseline
#   2. Random Forest       — ensemble of decision trees, robust
#   3. XGBoost            — gradient boosting, usually very accurate
#   4. SVM                — finds optimal decision boundary
# ───────────────────────────────────────────────────"""

print("\n🚀 Training 4 machine learning models...\n")

# Define all 4 models in one dictionary
models = {
    'Logistic Regression': LogisticRegression(
        random_state = RANDOM_STATE,
        max_iter     = 1000,
        C            = 1.0
    ),
    'Random Forest': RandomForestClassifier(
        n_estimators = 200,
        max_depth    = 8,
        random_state = RANDOM_STATE,
        n_jobs       = -1
    ),
    'XGBoost': XGBClassifier(
        n_estimators = 200,
        max_depth    = 4,
        learning_rate= 0.1,
        random_state = RANDOM_STATE,
        eval_metric  = 'logloss',
        verbosity    = 0
    ),
    'SVM': SVC(
        kernel       = 'rbf',
        C            = 1.0,
        probability  = True,   # needed for ROC-AUC calculation
        random_state = RANDOM_STATE
    )
}

# Store all results here
results = {}
trained_models = {}

for model_name, model in models.items():
    print(f"  Training {model_name}...", end=" ")
    t0 = time.time()

    # Logistic Regression and SVM need scaled data
    if model_name in ['Logistic Regression', 'SVM']:
        model.fit(X_train_scaled, y_train)
        y_pred      = model.predict(X_test_scaled)
        y_pred_prob = model.predict_proba(X_test_scaled)[:, 1]
    else:
        # Tree-based models work fine without scaling
        model.fit(X_train, y_train)
        y_pred      = model.predict(X_test)
        y_pred_prob = model.predict_proba(X_test)[:, 1]

    elapsed = time.time() - t0

    # Calculate metrics
    acc  = accuracy_score(y_test, y_pred)
    prec = precision_score(y_test, y_pred, zero_division=0)
    rec  = recall_score(y_test, y_pred, zero_division=0)
    f1   = f1_score(y_test, y_pred, zero_division=0)
    auc  = roc_auc_score(y_test, y_pred_prob)

    results[model_name] = {
        'accuracy'   : acc,
        'precision'  : prec,
        'recall'     : rec,
        'f1_score'   : f1,
        'roc_auc'    : auc,
        'y_pred'     : y_pred,
        'y_pred_prob': y_pred_prob,
    }
    trained_models[model_name] = model

    print(f"done in {elapsed:.1f}s  |  "
          f"Acc: {acc:.3f}  AUC: {auc:.3f}")

print("\n  ✅ All models trained!")

