"""
# ────────────────────────────────────────────────────────────────
# CELL 10 — ROC-AUC Curves
#
# ROC = Receiver Operating Characteristic
# AUC = Area Under the Curve (0.5 = random, 1.0 = perfect)
#
# The curve shows the trade-off between:
#   True Positive Rate  (catching actual disease cases)
#   False Positive Rate (flagging healthy patients as sick)
#
# Doctors prefer high True Positive Rate (don't miss sick patients)
# ──────────────────────────────────────────────"""

fig, ax = plt.subplots(figsize=(9, 7))

for i, model_name in enumerate(model_names):
    y_prob   = results[model_name]['y_pred_prob']
    fpr, tpr, _ = roc_curve(y_test, y_prob)
